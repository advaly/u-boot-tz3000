#ifndef _TZ3000_CPU_H_
#define _TZ3000_CPU_H_

#define TZ3000_CPU_BASE			0x0f100000

/* TZ3000 SCU */
#define TZ3000_SCU_BASE			TZ3000_CPU_BASE
#define TZ3000_SCU_CONTROL		(TZ3000_SCU_BASE + 0x00)

#define SCU_ENABLE			0x1

#define TZ3000_SCU_INV_ALL		(TZ3000_SCU_BASE + 0x0c)

#define SCU_INV_ALL_CPU_WAYS		0x0000ffff

/* TZ3000 IC */
#define TZ3000_IC_BASE			(TZ3000_CPU_BASE + 0x0100)

/* TZ3000 GLOBAL TIMER */
#define TZ3000_GTMR_BASE		(TZ3000_CPU_BASE + 0x0200)
#define TZ3000_GTMR_COUNTER_L		(TZ3000_GTMR_BASE + 0x00)
#define TZ3000_GTMR_COUNTER_H		(TZ3000_GTMR_BASE + 0x04)
#define TZ3000_GTMR_CONTROL		(TZ3000_GTMR_BASE + 0x08)

/* TZ3000 PRIVATE TIMER */
#define TZ3000_PTMR_BASE		(TZ3000_CPU_BASE + 0x0600)
#define TZ3000_PTMR_LOAD		(TZ3000_PTMR_BASE + 0x00)
#define TZ3000_PTMR_COUNTER		(TZ3000_PTMR_BASE + 0x04)
#define TZ3000_PTMR_CONTROL		(TZ3000_PTMR_BASE + 0x08)

/* TZ3000 L2 CACHE */
#define TZ3000_L2_BASE			(TZ3000_CPU_BASE + 0x2000)

#endif	/*_TZ3000_CPU_H_*/
