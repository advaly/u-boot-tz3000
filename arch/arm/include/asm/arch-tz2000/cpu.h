#ifndef _TZ2000_CPU_H_
#define _TZ2000_CPU_H_

#define TZ2000_CPU_BASE			0x0f100000

/* TZ2000 SCU */
#define TZ2000_SCU_BASE			TZ2000_CPU_BASE
#define TZ2000_SCU_CONTROL		(TZ2000_SCU_BASE + 0x00)

#define SCU_ENABLE			0x1

#define TZ2000_SCU_INV_ALL		(TZ2000_SCU_BASE + 0x0c)

#define SCU_INV_ALL_CPU_WAYS		0x0000ffff

/* TZ2000 GLOBAL TIMER */
#define TZ2000_GTMR_BASE		(TZ2000_CPU_BASE + 0x0200)
#define TZ2000_GTMR_COUNTER_L		(TZ2000_GTMR_BASE + 0x00)
#define TZ2000_GTMR_COUNTER_H		(TZ2000_GTMR_BASE + 0x04)
#define TZ2000_GTMR_CONTROL		(TZ2000_GTMR_BASE + 0x08)

/* TZ2000 PRIVATE TIMER */
#define TZ2000_PTMR_BASE		(TZ2000_CPU_BASE + 0x0600)
#define TZ2000_PTMR_LOAD		(TZ2000_PTMR_BASE + 0x00)
#define TZ2000_PTMR_COUNTER		(TZ2000_PTMR_BASE + 0x04)
#define TZ2000_PTMR_CONTROL		(TZ2000_PTMR_BASE + 0x08)

#endif	/*_TZ2000_CPU_H_*/
